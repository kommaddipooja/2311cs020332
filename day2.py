my_list = [10, 20, 30, 40, 50]
my_tuple = ("apple", "banana", "cherry", "date", "elderberry")
my_dict = {"name": "Alice", "age": 25, "city": "New York", "job": "Engineer", "hobby": "Painting"}

print("List Examples:")
print("Element at index 2:", my_list[2])
print("Slice from index 1 to 3:", my_list[1:4])

print("\nTuple Examples:")
print("Element at index 0:", my_tuple[0])
print("Slice from index 2 to the end:", my_tuple[2:])

print("\nDictionary Examples:")
print("Value for key 'name':", my_dict["name"])
print("Value for key 'city':", my_dict["city"])
